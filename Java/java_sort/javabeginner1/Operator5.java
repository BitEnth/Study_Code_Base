package javabeginner1;

public class Operator5 {
	public static void main(String[] args) {
		int a=13&5;
		System.out.println("13&5="+a);
		int b=13|5;
		System.out.println("13|5="+b);
		int c=13^5;
		System.out.println("13^5="+c);
		int d=~5,e=~-5;
		System.out.println("~5="+d);
		System.out.println("~-5="+e);
	}
}
